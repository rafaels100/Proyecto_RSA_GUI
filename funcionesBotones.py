def disponible(boton, asientos):
        retrieveBoton = boton["text"]
        if len(asientos) >= 5:
                alert("No se pueden seleccionar mas de 5 asientos. Comuniquese"\
                      +" con la aerolinea para adquirir un paquete empresarial.")
        else:
                if boton["bg"] == "green":
                        alert("Se selecciono el asiento {}".format(retrieveBoton))
                        asientos.append(int(retrieveBoton))
                        boton["bg"] = "red"
                else:
                        alert("El asiento seleccionado esta ocupado.")
        
